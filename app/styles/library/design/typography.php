<h3>Font Weights</h3>
<p>There are two font weights available to use &mdash; Regular and Bold. They can be applied with <code class="il">.fontWeight--1</code> and <code class="il">.fontWeight--2</code> respectively.</p>
<div class="well well__inline no--pad">
    <div class="well__row">
        <div class="well__section">
