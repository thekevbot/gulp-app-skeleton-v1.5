<h1>Typography</h1>
<h3>Typefaces</h3>
<p>The primary typeface is Harmonia Sans Pro &mdash; a robust typeface with legible numbers that renders well at all sizes. It is soft and friendly, yet plain enough to get out of the way and let the user's designs shine.</p>
